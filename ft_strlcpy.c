#include <libft.h>

size_t	ft_strlcpy(char *to, const char *from, size_t n)
{
	size_t	i;

	i = 0;
	while ((i < n) && from[i])
	{
		to[i] = from[i];
		i++;
	}
	to[i] = '\0';
	return (i);
}
