int	ft_atoi(char *s)
{
	unsigned int	rez;
	int				sign;

	while(*s >= '\t' && *s <= '\r')
		s++;
	sign = 0;
	if(*s == '-')
		sign = 1;
	if(*s == '+' || *s == '-')
		s++;
	rez = 0;
	while(*s >= '0' && *s <= '9')
	{
		rez = rez * 10 + (*s - '0');
		s++;
	}
	if(sign == 1)
		return (-rez);
	return (rez);
}
